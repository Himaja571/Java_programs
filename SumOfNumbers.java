public class SumOfNumbers {
    public static void main(String[] args) {
        // Define two numbers
        int number1 = 15;
        int number2 = 25;
        
        // Calculate the sum
        int sum = number1 + number2;
        
        // Display the result
        System.out.println("The sum of " + number1 + " and " + number2 + " is: " + sum);
    }
}